    loadProgress,
    PollingAlarm,
  },
  data() {
    return {
      back: null,
      timer: null,
      TIME : null
    };
  },
  created() {
    const restartTime = "23:59:59"
    const todayRestartTime = `YYYY-MM-DD ${restartTime}`
    const date = moment().format(todayRestartTime)
    let timeout = moment(date).format('x') - moment().format('x')
    if(timeout <= 0) {
      timeout = moment(date).add(1, 'day').format('x') - moment().format('x')
    }
    this.timer = setTimeout(() => {
      this.$router.go(0)
    }, timeout)
    setTimeout(this.event , 1000);
    },
    watch: {
        "$store.state.loading": function() {
            console.log("?????????????????????????")
            console.log(this.$store.state.loading)
            if(this.$store.state.loading){
                this.TIME = Date.parse(new Date())
            }else{
                this.TIME = null
                
            }
        }
    },
    methods: {
        event(){
            if(this.TIME == null)return
            if(Date.parse(new Date()) - this.TIME > 3 * 60){
                this.$store.state.loading = false
            }
        }
    },
  computed: {
    ...mapState({
      getSolutionEnable: (state) => state.route.routeList, //獲取狀態
    }),
    getBackImage() {
      return { backgroundImage: `url(${this.getStoreback})` };
    },
    getStoreback() {
      return this.$store.state.backData.backGroundImage;
    },
    loading() {
      return this.$store.state.loading;
    },
    loadingProgress(){
      return this.$store.state.loadingProgress;
    },
    completeLoading(){
      return this.$store.state.completeLoading;
    },
    totalLoading(){
      return this.$store.state.totalLoading;
    },
    message(){
      return this.$store.state.message;
    },
    PollingAlarm(){
      return this.$store.state.polling.show;
    },
  },
};
